package cast

import (
	"fmt"
	"reflect"
	"strings"
	"time"
)

/*
ToSlice casts an interface to a []interface{} type.
*/
func ToSlice(i interface{}) ([]interface{}, error) {
	var s []interface{}

	switch t := i.(type) {
	case []interface{}:
		return append(s, t...), nil
	case []map[string]interface{}:
		for _, u := range t {
			s = append(s, u)
		}
		return s, nil
	default:
		return s, fmt.Errorf("unable to cast %#v of type %T to []interface{}", i, i)
	}
}

/*
ToBoolSlice casts an interface to a []bool type.
*/
func ToBoolSlice(i interface{}) ([]bool, error) {
	if i == nil {
		return []bool{}, fmt.Errorf("unable to cast %#v of type %T to []bool", i, i)
	}

	switch v := i.(type) {
	case []bool:
		return v, nil
	}

	kind := reflect.TypeOf(i).Kind()
	switch kind {
	case reflect.Slice, reflect.Array:
		s := reflect.ValueOf(i)
		a := make([]bool, s.Len())
		for j := 0; j < s.Len(); j++ {
			val, err := ToBool(s.Index(j).Interface())
			if err != nil {
				return []bool{}, fmt.Errorf("unable to cast %#v of type %T to []bool", i, i)
			}
			a[j] = val
		}
		return a, nil
	default:
		return []bool{}, fmt.Errorf("unable to cast %#v of type %T to []bool", i, i)
	}
}

/*
ToStringSlice casts an interface to a []string type.
*/
func ToStringSlice(i interface{}) ([]string, error) {
	var a []string

	switch t := i.(type) {
	case []interface{}:
		for _, val := range t {
			v, _ := ToString(val)
			a = append(a, v)
		}
		return a, nil
	case []string:
		return t, nil
	case string:
		return strings.Fields(t), nil
	case interface{}:
		str, err := ToString(t)
		if err != nil {
			return a, fmt.Errorf("unable to cast %#v of type %T to []string", i, i)
		}
		return []string{str}, nil
	default:
		return a, fmt.Errorf("unable to cast %#v of type %T to []string", i, i)
	}
}

